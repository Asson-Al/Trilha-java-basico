package sintaxejava;

public class TiposVariaveis {
    public static void main(String[] args) {
        //tipos primitivos.

        //String é ustilizado para atribuir palavras e tem que ser colocado entre aspas duplas.

        String nome = "Pedro";

        double salarioMinimo = 2500.50;

        //float tem que ter o F no final para indicar que é um float.

        float pi = 3.14f;

        //long tem que ter o L no final para indicar que é um long.

        long numeroLongo = 1231241L;

        short numeroCurto = 1;
        int numeroNormal = numeroCurto;

        //(short)= cast.
        short numeroCurto2 = (short) numeroNormal;

        int numero = 5;

        //usando final valor que não pode ser alterado é chamado de final = a variavel é inplementada em caixa alta.

        final double VALOR_DE_PI = 3.14;


    }
}
